#!/bin/sh
# run-all-in-docker.sh — 在 wrk 容器内顺序执行所有压测场景
# 用于避免 Docker Desktop Windows 上重复创建容器的网络抖动

set -e

HOST="${WRK_HOST:-http://api-gateway:8000}"
TOKEN="${WRK_TOKEN:-}"
DUR="${DURATION:-15s}"
SCRIPTS="/scripts"

echo "=============================================="
echo "  Blog Community — WRK QPS 压力测试 (容器内)"
echo "=============================================="
echo "目标: $HOST"
echo "Token: ${TOKEN:+${TOKEN:0:20}...}"
echo ""

# 1. 公开读
echo "###############################################################################"
echo "#  测试 1/5: 公开读接口 (pub-read) — t4 c100 ${DUR}"
echo "###############################################################################"
echo ""
wrk -t4 -c100 -d"${DUR}" --latency -s "${SCRIPTS}/pub-read.lua" "$HOST" 2>&1
echo ""

# 2. 公开读高并发
echo "###############################################################################"
echo "#  测试 2/5: 公开读高并发 (pub-read-hc) — t8 c500 ${DUR}"
echo "###############################################################################"
echo ""
wrk -t8 -c500 -d"${DUR}" --latency -s "${SCRIPTS}/pub-read.lua" "$HOST" 2>&1
echo ""

# 3. 认证读
echo "###############################################################################"
echo "#  测试 3/5: 认证读接口 (auth-read) — t4 c100 ${DUR}"
echo "###############################################################################"
echo ""
WRK_TOKEN="$TOKEN" wrk -t4 -c100 -d"${DUR}" --latency -s "${SCRIPTS}/auth-read-ok.lua" "$HOST" 2>&1
echo ""

# 4. 认证写
echo "###############################################################################"
echo "#  测试 4/5: 认证写接口 (auth-write) — t2 c50 ${DUR}"
echo "###############################################################################"
echo ""
WRK_TOKEN="$TOKEN" wrk -t2 -c50 -d"${DUR}" --latency -s "${SCRIPTS}/auth-write-ok.lua" "$HOST" 2>&1
echo ""

# 5. 混合负载
echo "###############################################################################"
echo "#  测试 5/5: 混合负载 (mixed) — t4 c200 30s"
echo "###############################################################################"
echo ""
WRK_TOKEN="$TOKEN" wrk -t4 -c200 -d30s --latency -s "${SCRIPTS}/mixed-ok.lua" "$HOST" 2>&1
echo ""

# 阶梯加压读
echo "###############################################################################"
echo "# 阶梯加压: 公开读 (50/100/200/500/1000 并发)"
echo "###############################################################################"
for c in 50 100 200 500 1000; do
    echo "--- 并发: ${c} ---"
    wrk -t4 -c"${c}" -d10s --latency -s "${SCRIPTS}/pub-read.lua" "$HOST" 2>&1 | grep -E "requests in|Requests/sec|Latency.*[0-9]"
    sleep 2
done
echo ""

# 阶梯加压写
echo "###############################################################################"
echo "# 阶梯加压: 认证写 (10/30/50/100 并发)"
echo "###############################################################################"
for c in 10 30 50 100; do
    echo "--- 并发: ${c} ---"
    WRK_TOKEN="$TOKEN" wrk -t2 -c"${c}" -d10s --latency -s "${SCRIPTS}/auth-write-ok.lua" "$HOST" 2>&1 | grep -E "requests in|Requests/sec|Latency.*[0-9]"
    sleep 2
done
echo ""

echo "=============================================="
echo "  压测完成!"
echo "=============================================="
